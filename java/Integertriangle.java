class Integertriangle
{
        public static void main(String args[])
        {
                Integer i, j, k = 5;
                for (i = 1; i <= k; i++) {
                        for (j = 1; j <= k; j++)
                        {
                                System.out.print(i + "\t");
                        }
                        System.out.println();
                }
        }
}